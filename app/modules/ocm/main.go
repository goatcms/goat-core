// Package ocm provide Open Container middleware
// Open Container Initiative
// The Open Container Initiative is an open governance structure for the express purpose of creating open industry standards around container formats and runtimes.
// Established in June 2015 by Docker and other leaders in the container industry,
// the OCI currently contains two specifications: the Runtime Specification (runtime-spec)
// and the Image Specification (image-spec). The Runtime Specification outlines how to run a
// “filesystem bundle” that is unpacked on disk. At a high-level an OCI implementation would
// download an OCI Image then unpack that image into an OCI Runtime filesystem bundle.
// At this point the OCI Runtime Bundle would be run by an OCI Runtime.
// Website: https://opencontainers.org/
